// Navbar elevation
window.addEventListener('scroll', () => {
  document.getElementById('navbar').classList.toggle('elevated', window.scrollY > 30);
});

// Project filter
function filterProjects(f) {
  document.querySelectorAll('.filter-btn').forEach(b => b.classList.toggle('active', b.dataset.filter === f));
  document.querySelectorAll('.proj-card').forEach(c => {
    c.style.display = (f === 'all' || c.dataset.cat.includes(f)) ? '' : 'none';
  });
}

// Scroll reveal
const io = new IntersectionObserver((entries) => {
  entries.forEach(e => {
    if (e.isIntersecting) { e.target.classList.add('in'); io.unobserve(e.target); }
  });
}, { threshold: 0.08, rootMargin: '0px 0px -40px 0px' });

document.querySelectorAll('.reveal, .timeline-item, .proj-card, .skill-cat').forEach(el => io.observe(el));


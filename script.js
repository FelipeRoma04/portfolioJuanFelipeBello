let lang = localStorage.getItem('jfb-lang') || 'en';

function setLang(l) {
  lang = l;
  localStorage.setItem('jfb-lang', l);
  document.documentElement.lang = l;
  document.getElementById('btn-en').classList.toggle('active', l === 'en');
  document.getElementById('btn-es').classList.toggle('active', l === 'es');
  const t = T[l];

  // text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const v = t[el.dataset.i18n];
    if (v !== undefined) el.textContent = v;
  });

  // innerHTML (bold, italic, br)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const v = t[el.dataset.i18nHtml];
    if (v !== undefined) el.innerHTML = v;
  });

  // tl-company multi-line
  ['exp3_co','exp4_co'].forEach(key => {
    document.querySelectorAll(`[data-i18n="${key}"]`).forEach(el => {
      if (t[key]) el.innerHTML = t[key].replace('\n','<br>');
    });
  });

  // hero_role with newline as <br>
  document.querySelectorAll('[data-i18n="hero_role"]').forEach(el => {
    if (t['hero_role']) el.innerHTML = t['hero_role'].replace('\n','<br>');
  });

  // bullets with innerHTML
  ['exp1','exp2','exp3','exp4'].forEach(e => {
    for (let i = 1; i <= 9; i++) {
      const k = `${e}_b${i}`;
      if (!t[k]) break;
      document.querySelectorAll(`[data-i18n="${k}"]`).forEach(el => { el.innerHTML = t[k]; });
    }
  });
}

// Mobile menu
